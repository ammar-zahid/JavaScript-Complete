let fruits = ["banana", "apple", "mango"];
console.log(fruits);
console.log(fruits.length);

// push
fruits.push("cherry");
console.log(fruits);

// pop
let poppedFruit = fruits.pop();
console.log(poppedFruit);
console.log(fruits);

// shift
let removedFruit = fruits.shift();
console.log(removedFruit);
console.log(fruits);

// unshift
fruits.unshift("grapes");
console.log(fruits);