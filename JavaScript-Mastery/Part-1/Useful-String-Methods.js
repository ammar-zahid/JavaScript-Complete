// trim()

let name = "   Captain   ";
console.log(name);
console.log(name.length);

let trimmedName = name.trim();
console.log(trimmedName);
console.log(trimmedName.length);

// toUpperCase()

let name2 = "artistic";
console.log(name2);
let capitalLetters = name2.toUpperCase();
console.log(capitalLetters);

// toLowerCase()

let name3 = "CAPTAIN";
console.log(name3);
name3 = name3.toLowerCase();
console.log(name3);

// slice()

let city = "Karachi";
console.log(city);
city = city.slice(2,6);
